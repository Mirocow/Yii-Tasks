<?php

class ErrorController extends Controller {

    public function actionCode() {
        if ($error = Yii::app()->errorHandler->error) {
            if (Yii::app()->request->isAjaxRequest)
                echo $error['message'];
            else
                $this->render('code', $error);
        }
    }

